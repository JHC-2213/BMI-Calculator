    public String formattwoDecimal(double decimal){
       String formated = String.format("%.2f", decimal);
       return formated;
    }
    
    public void get(){
        Person person = new Person(Double.parseDouble(jTextField1.getText().toString()),
                Double.parseDouble(jTextField2.getText().toString()));
        
        double meterheight = person.Mheight;
        jTextField3.setText( formattwoDecimal(meterheight));
        double mass = person.mass;
        jTextField4.setText( formattwoDecimal(mass));
        double bmi = person.BMI;
        jTextField5.setText( formattwoDecimal(bmi));
        double inch = person.HinInch;
        jTextField6.setText( formattwoDecimal(inch));
        jTextArea1.setText(person.BMIInfo);
    }
    
